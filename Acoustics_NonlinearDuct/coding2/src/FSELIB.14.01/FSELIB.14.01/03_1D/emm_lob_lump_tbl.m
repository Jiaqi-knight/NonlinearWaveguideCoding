function elm_mm = emm_lob_lump(m)

%----------------------------------------------------
% lumped lobatto element mass matrix for an mth-order
% polynomial expansion
%
% m = 1,2,...,5
%---------------------------------------------------

%-------
% trap
%-------

if(m>5)

  disp (' emm_lob_lump: maximum polynomial order is 5');
  error(' chosen order is not available');

end

%-------
if(m==1)
%-------

elm_mm = [1 0 ; 0 1];

%-------
elseif(m==2)
%-------

elm_mm = [
1/3 0 0; 0 1.333333333333  0; 0 0 1/3];

%-------
elseif(m==3)
%-------

elm_mm =[
0.166666666 0 0 0;
0 0.833333333333 0 0 ;
0 0 0.833333333333 0;
0 0 0 0.16666666666];

%-------
elseif(m==4)
%-------

elm_mm =[
 0.1  0  0  0  0;
 0  0.544444444444  0 0 0;
 0 0  0.711111111111  0 0;
 0 0 0 0.54444444444 0;
 0 0 0 0 0.1];

%-------
elseif(m==5)
%-------

elm_mm =[
0.0666666666 0 0 0 0 0;
0 0.378474950790 0 0 0 0;
0 0 0.554858386517 0 0 0;
0 0 0 0.554858386517 0 0;
0 0 0 0 0.378474950790 0;
0 0 0 0 0 0.066666666666];

%--
end
%--

return;
